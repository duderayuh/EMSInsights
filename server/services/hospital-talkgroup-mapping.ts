export function getHospitalInfo(
  talkgroupId: string,
): HospitalTalkgroupInfo | null {
  return HOSPITAL_TALKGROUPS[talkgroupId] || null;
}

export function getEmsChannelName(channelId: string): string {
  return EMS_CHANNEL_MAPPING[channelId] || `Channel ${channelId}`;
}

export function formatHospitalDisplay(
  talkgroupId: string,
  emsChannelId?: string,
): string {
  const hospitalInfo = getHospitalInfo(talkgroupId);

  if (!hospitalInfo) {
    return `Talkgroup ${talkgroupId}`;
  }

  let display = hospitalInfo.hospitalName;

  if (emsChannelId) {
    const emsChannel = getEmsChannelName(emsChannelId);
    display += ` ‚Üê ${emsChannel}`;
  }

  return display;
}

export function isHospitalTalkgroup(talkgroupId: string): boolean {
  return talkgroupId in HOSPITAL_TALKGROUPS;
}
